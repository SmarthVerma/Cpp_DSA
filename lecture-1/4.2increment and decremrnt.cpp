#include <iostream>
using namespace std;

int main (){

   // increment

    int x;

    x=3;
    cout<<x<<endl;
    x++; // post increment
    cout<<x<<endl;
    ++x; // pre increment
    cout<<x<<endl;

// decrement

cout<<endl;
    x--;
    cout<<x<<endl;
    --x;
    cout<<x<<endl;

    // difference between pre and post 
    
    
cout<<endl; 
    cout<<x++<<endl;
    cout<<x<<endl; // ab post ka jalwan dekhyga

cout<<endl;
    cout<<++x<<endl; // pre ka jalwa
    cout<<x<<endl;


}